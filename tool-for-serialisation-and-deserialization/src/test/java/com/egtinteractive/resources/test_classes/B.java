package com.egtinteractive.resources.test_classes;

public class B {
    private int numberInB;

    public B(final int numberInB) {
	super();
	this.numberInB = numberInB;
    }

    @Override
    public int hashCode() {
	final int prime = 31;
	int result = 1;
	result = prime * result + numberInB;
	return result;
    }

    @Override
    public boolean equals(final Object obj) {
	if (this == obj)
	    return true;
	if (obj == null)
	    return false;
	if (getClass() != obj.getClass())
	    return false;
	B other = (B) obj;
	if (numberInB != other.numberInB)
	    return false;
	return true;
    }
}